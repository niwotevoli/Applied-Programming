#include <iostream>
#include <cmath>
#include <cstdio>
using namespace std;

int main() {
    double a, b, c;
    cout << "Данная программа вычисляет площадь треугольника по трем его сторонам." << endl << "Введите длины трех сторон треугольника (через пробел или по-очередно): ";
    cin >> a >> b >> c;

    // Проверка на существование треугольника
    if (a <= 0 || b <= 0 || c <= 0 || 
        (a + b <= c) || (a + c <= b) || (b + c <= a)) {
        cout << "Треугольник с такими сторонами не существует!" << endl;
        return 1;
    }

    // Вычисление полупериметра
    double p = (a + b + c) / 2;
    // Вычисление площади по формуле Герона
    double area = sqrt(p * (p - a) * (p - b) * (p - c));

    // Вывод с двумя знаками после запятой используя printf
    printf("Площадь треугольника: %.2f\n", area);
    return 0;
}
